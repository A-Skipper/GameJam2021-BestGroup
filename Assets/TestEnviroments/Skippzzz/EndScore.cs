using UnityEngine;

public class EndScore : MonoBehaviour
{
    public int score;

    private static EndScore instance = null;
    public static EndScore Instance
    {
        get { return instance; }
    }
    void Awake()
    {
        if (instance != null && instance != this)
        {
            Destroy(this.gameObject);
            return;
        }
        else
        {
            instance = this;
        }
        DontDestroyOnLoad(this.gameObject);
    }
}
